

public class HelloWorld {

	public static void main(String[] args) {
		System.out.println("Hello");

		int x=4; //type casting- Widening (Lower to higher)
		double y =x;
		System.out.println(y);
		double md=3.14; //type casting- narrowing (higher to lower-manual type casting)
		int mi=(int) md;
		System.out.println(mi);
	}

	
}
 
